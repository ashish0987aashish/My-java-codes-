

// BITWISE OPERATOR  CHECKS BOTH THE CONDITIONS
  
class Testsecondnbw
{
 
    public static void main(String [] args)
   {
     int a=10, b=15;
   
    if(++a>10 & ++b<15)
    {
       a++;
    }
  
   else
   {
     b++;
   }
    
   System.out.println(a+" * * * * * * "+b);
  }
 
}

     

 


  